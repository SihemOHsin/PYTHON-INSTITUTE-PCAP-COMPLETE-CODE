def isValidSudoku(board):

    for i in range(9):
        row = {}
        column = {}
        block = {}
        row_cube = 3 * (i//3)
        column_cube = 3 * (i%3)
        for j in range(9):
            if board[i][j] in row:
                return "No"
            row[board[i][j]] = 1
            if board[j][i] in column:
                return "No"
            column[board[j][i]] = 1
            rc= row_cube+j//3
            cc = column_cube + j%3
            if board[rc][cc] in block :
                return "No"
            block[board[rc][cc]]=1
    return "Yes"
